public class Calculadora {
    public int sumaEntero(int a, int b){
        return a + b;
    }
    public int restaEntero(int a, int b){
        return a - b;
    }
    public int multiEntero(int a, int b){
        return a * b;
    }
    public int divisionEntero(int a, int b){
        return a / b;
    }
    public double sumaDecimal(double a, double b){
        return a + b;
    }
    public double restaDecimal(double a, double b){
        return a - b;
    }
    public double multiDecimal(double a, double b){
        return a * b;
    }
    public double divisionDecimal(double a, double b){
        return a / b;
    }
}
